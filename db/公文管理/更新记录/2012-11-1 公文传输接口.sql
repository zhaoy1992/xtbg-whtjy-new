DROP TABLE OA_ARCHIVE_DISPATCH CASCADE CONSTRAINTS;

/*==============================================================*/
/* Table: OA_ARCHIVE_DISPATCH                                   */
/*==============================================================*/
CREATE TABLE OA_ARCHIVE_DISPATCH 
(
   ID                   VARCHAR2(50)         NOT NULL,
   BUSI_ID              VARCHAR2(50),
   INS_ID               VARCHAR2(50),
   TITLE                VARCHAR2(200),
   CAPTION              VARCHAR2(200),
   URGENCY_GRADE        VARCHAR2(200),
   ARCHIVE_NO           VARCHAR2(200),
   DISPATCH_TYPE        VARCHAR2(50),
   KEY_WORD             VARCHAR2(50),
   EFFECT_DATE          VARCHAR2(50),
   SENDER               VARCHAR2(50),
   SECRECY_GRADE        VARCHAR2(50),
   SEND_ORG_ID          VARCHAR2(50),
   SEND_ORG_ADDR        VARCHAR2(50),
   SEND_ORG_NAME        VARCHAR2(200),
   SEND_ATTACH_ID       VARCHAR2(50),
   CREATE_DATE          DATE,
   IS_SNED_ATTACH       VARCHAR2(50),
   ISDXTX               VARCHAR2(50),
   MSG_TXT              VARCHAR2(500),
   UNION_ARCHIVE        VARCHAR2(50),
   REMARK1              VARCHAR2(50),
   REMARK2              VARCHAR2(50),
   CONSTRAINT PK_OA_ARCHIVE_DISPATCH PRIMARY KEY (ID)
);

COMMENT ON TABLE OA_ARCHIVE_DISPATCH IS
'公文发送端,公文要素表';

