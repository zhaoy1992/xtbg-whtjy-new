'主题词';

COMMENT ON COLUMN OA_ARCHIVE_DISPATCH.URGENCY_GRADE IS
'紧急程度';

COMMENT ON COLUMN OA_ARCHIVE_DISPATCH.ARCHIVE_NO IS
'发文号';

COMMENT ON COLUMN OA_ARCHIVE_DISPATCH.DISPATCH_TYPE IS
'文种';

COMMENT ON COLUMN OA_ARCHIVE_DISPATCH.KEY_WORD IS
'关键字';

COMMENT ON COLUMN OA_ARCHIVE_DISPATCH.EFFECT_DATE IS
'保密期限';

COMMENT ON COLUMN OA_ARCHIVE_DISPATCH.SENDER IS
'签发人';

