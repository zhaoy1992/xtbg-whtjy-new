package com.chinacreator.xtbg.pub.flowinfoconif.entity;

/**
 * 流程定义信息
 * @author Administrator
 *
 */
public class FlowDefInfoBean {
	/**
	 * 流程定义主键
	 */
	private String def_id;
	
	/**
	 * 流程定义ID
	 */
	private String flow_defid;
	
	/**
	 * 流程ID
	 */
	private String flow_id;
	
	/**
	 * 流程版本
	 */
	private String flow_version;
	
	/**
	 * 流程包
	 */
	private String package_id;
	
	/**
	 * 是否生效
	 */
	private String is_valid;
	
	/**
	 * 创建时间
	 */
	private String create_time;

	
	
	public String getDef_id() {
		return def_id;
	}

	public void setDef_id(String defId) {
		def_id = defId;
	}

	public String getFlow_defid() {
		return flow_defid;
	}

	public void setFlow_defid(String flowDefid) {
		flow_defid = flowDefid;
	}

	public String getFlow_id() {
		return flow_id;
	}

	public void setFlow_id(String flowId) {
		flow_id = flowId;
	}

	public String getFlow_version() {
		return flow_version;
	}

	public void setFlow_version(String flowVersion) {
		flow_version = flowVersion;
	}

	public String getPackage_id() {
		return package_id;
	}

	public void setPackage_id(String packageId) {
		package_id = packageId;
	}

	public String getIs_valid() {
		return is_valid;
	}

	public void setIs_valid(String isValid) {
		is_valid = isValid;
	}

	public String getCreate_time() {
		return create_time;
	}

	public void setCreate_time(String createTime) {
		create_time = createTime;
	}
	
	
}
