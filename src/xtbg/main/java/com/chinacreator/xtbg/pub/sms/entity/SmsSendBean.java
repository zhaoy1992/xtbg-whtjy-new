public class SmsSendBean extends XtDbBaseBean{
	
	
	public SmsSendBean() {
		super("T_SMSSEND");
	}

	private String receive; // 接收号码
	private String content; // 内容
	private String sendtype; // 0立即发送(默认)，1定时发送
	private String sendtime; // 定时发送时间
	private String instime; // 入表时间
	private String state; // (可回写) 0待发送(默认)，1发送成功，2发送失败，3正在发送
	private String systime; // 发送时间
	private String sendphone; // 发送号码
	private String remark; // 备注
	private String UserName;//发送者的中文名字
	/**
	 *<b>Summary: 获取发送者的中文名字</b>
	 */
	public String getUserName() {
		return UserName;
	}
	/**
	 *<b>Summary: 设置发送者的中文名字</b>
	 */
	public void setUserName(String userName) {
		UserName = userName;
	}

	/**
	 *<b>Summary: 获取接收号码</b>
	 */
	public String getReceive() {
		return receive;
	}

	/**
	 *<b>Summary: 设置接收号码</b>
	 */
	public void setReceive(String receive) {
		this.receive = receive;
	}

	/**
	 *<b>Summary: 获取内容</b>
	 */
	public String getContent() {
		return content;
	}

	/**
	 *<b>Summary: 设置内容</b>
	 */
	public void setContent(String content) {
		this.content = content;
	}

	/**
	 *<b>Summary: 获取0立即发送(默认)，1定时发送</b>
	 */
	public String getSendtype() {
		return sendtype;
	}

	/**
	 *<b>Summary: 设置0立即发送(默认)，1定时发送</b>
	 */
	public void setSendtype(String sendtype) {
		this.sendtype = sendtype;
	}

	/**
	 *<b>Summary: 获取定时发送时间</b>
	 */
	public String getSendtime() {
		return sendtime;
	}

	/**
	 *<b>Summary: 设置定时发送时间</b>
	 */
	public void setSendtime(String sendtime) {
		this.sendtime = sendtime;
	}

	/**
	 *<b>Summary: 获取入表时间</b>
	 */
	public String getInstime() {
		return instime;
	}

	/**
	 *<b>Summary: 设置入表时间</b>
	 */
	public void setInstime(String instime) {
		this.instime = instime;
	}

	/**
	 *<b>Summary: 获取(可回写) 0待发送(默认)，1发送成功， 2发送失败，3正在发送</b>
	 */
	public String getState() {
		return state;
	}

	/**
	 *<b>Summary: 设置(可回写) 0待发送(默认)，1发送成功， 2发送失败，3正在发送</b>
	 */
	public void setState(String state) {
		this.state = state;
	}

	/**
	 *<b>Summary: 获取发送时间</b>
	 */
	public String getSystime() {
		return systime;
	}

	/**
	 *<b>Summary: 设置发送时间</b>
	 */
	public void setSystime(String systime) {
		this.systime = systime;
	}

	/**
	 *<b>Summary: 获取发送号码</b>
	 */
	public String getSendphone() {
		return sendphone;
	}

	/**
	 *<b>Summary: 设置发送号码</b>
	 */
	public void setSendphone(String sendphone) {
		this.sendphone = sendphone;
	}

	/**
	 *<b>Summary: 获取备注</b>
	 */
	public String getRemark() {
		return remark;
	}

	/**
	 *<b>Summary: 设置备注</b>
	 */
	public void setRemark(String remark) {
		this.remark = remark;
	}
}
