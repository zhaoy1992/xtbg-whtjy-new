package com.chinacreator.business.common.exception;

public class SystemException  extends ExceptionAbstract {

	/**
	 * 
	 */
	private static final long serialVersionUID = 189185142114L;
	
	public SystemException(String errorCode, String errorMsg) {
		super(errorCode, errorMsg,"SYS");
	}
	
	public SystemException(Exception e){
		super(e);
	}
	public SystemException(String errorCode){
		super(errorCode, "","SYS");
	}
}
